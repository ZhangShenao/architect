package william.algo.list;

/**
 * @Author zhangshenao
 * @Date 2021-10-30
 * @Description
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
